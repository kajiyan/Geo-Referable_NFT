/**
 * Read a 24-bit little-endian value from the LUT
 * @param index - Entry index (0-900)
 * @returns 24-bit value as number
 */
export function readU24(index: number): number {
  const offset = index * 3;
  return (
    SIN_LUT[offset] |
    (SIN_LUT[offset + 1] << 8) |
    (SIN_LUT[offset + 2] << 16)
  );
}

/**
 * Calculate sine value using LUT with linear interpolation
 * Matches Fumi.sol _sin1e5 exactly
 *
 * @param angle1e4 - Angle in radians scaled by 1e4
 * @returns Sine value scaled by 1e5 (range: -100000 to 100000)
 */
export function sin1e5(angle1e4: number): number {
  const num = angle1e4 * DEG10_NUM;
  const deg10 = Math.floor(num / DEG10_DEN) % 3600; // [0..3599]
  const rem = num % DEG10_DEN; // remainder for interpolation
  const quad = Math.floor(deg10 / 900); // 0-3
  const pos = deg10 % 900; // 0-899

  const base = quad % 2 === 0 ? pos : 900 - pos;
  const b1 =
    quad % 2 === 0
      ? base < 900
        ? base + 1
        : 900
      : base > 0
        ? base - 1
        : 0;

  const v0 = readU24(base);
  const v1 = readU24(b1);

  // Linear interpolation
  const vi = v0 + Math.floor(((v1 - v0) * rem) / DEG10_DEN);

  // Quadrant adjustment
  return quad >= 2 ? -vi : vi;
}

/**
 * Calculate fade curve value (absolute sine over 0..π)
 * Used for wave amplitude modulation
 * Matches Fumi.sol _fade1e5 exactly
 *
 * @param i - Segment index
 * @param nSeg - Total number of segments
 * @returns Fade value scaled by 1e5 (range: 0 to 100000)
 */
export function fade1e5(i: number, nSeg: number): number {
  // π ≈ 355/113, scale by 1e4
  const angle1e4 = Math.floor((3_550_000 * i) / (113 * nSeg));
  const s = sin1e5(angle1e4);
  return Math.abs(s);
}

/**
 * Pre-calculate fade table for main waves (30 segments)
 * @returns Array of 30 fade values scaled by 1e5
 */
export function calculateMainFadeTable(): number[] {
  const fade: number[] = new Array(N_SEG + 1);
  fade[0] = 0;
  for (let k = 1; k < N_SEG; k++) {
    fade[k] = fade1e5(k, N_SEG);
  }
  fade[N_SEG] = 0;
  return fade;
}

